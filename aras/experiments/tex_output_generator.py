"""Generate per-experiment LaTeX section files under tex_output/.

Called by CoderAgent after run_experiments() completes.
All internal exceptions are caught so the pipeline never fails because of
tex generation -- the pipeline continues without tex output if anything
goes wrong.
"""

from __future__ import annotations

import json
import os
import re
from pathlib import Path
from typing import Any

from aras.utils.logging import get_logger

log = get_logger("tex-output-generator")


class ExperimentTexGenerator:
    """
    Generates per-experiment LaTeX section files under
    experiments/{slug}/tex_output/.
    """

    def __init__(self, bundle_dir: Path) -> None:
        self.bundle_dir = bundle_dir
        self.tex_output_dir = bundle_dir / "tex_output"
        self.tex_output_dir.mkdir(parents=True, exist_ok=True)

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def generate_all(
        self,
        *,
        manifest: dict[str, Any],
        aggregate_results: dict[str, Any],
        analysis: dict[str, Any] | None = None,
        paper_dir: Path | None = None,
    ) -> Path:
        """
        Generate one .tex file per experiment + combined file.

        Parameters
        ----------
        manifest : dict
            The experiment manifest (parsed manifest.json).
        aggregate_results : dict
            The aggregate results.json data (with "runs" list).
        analysis : dict, optional
            Analysis dict from AnalystAgent (contains "narrative", etc.)
        paper_dir : Path, optional
            If provided, figure paths are resolved relative to this
            directory (where paper.tex lives) instead of tex_output/.

        Returns
        -------
        Path
            Path to experiments_combined.tex.
        """
        input_lines: list[str] = []
        runs = aggregate_results.get("runs") or []
        runs_by_name: dict[str, dict[str, Any]] = {}
        for r in runs:
            name = r.get("name", "")
            if name:
                runs_by_name[name] = r

        experiments = manifest.get("experiments") or []
        for i, exp in enumerate(experiments, start=1):
            try:
                exp_name = exp.get("name", f"exp{i}")
                exp_dir = self.bundle_dir / exp_name

                # Per-experiment results from the run data
                run_data = runs_by_name.get(exp_name, {})
                exp_metrics = run_data.get("metrics") or {}

                # Also try loading per-experiment results.json written by runner
                per_exp_results_path = exp_dir / "results.json"
                per_exp_results: dict[str, Any] = {}
                if per_exp_results_path.exists():
                    try:
                        per_exp_results = json.loads(
                            per_exp_results_path.read_text(encoding="utf-8")
                        )
                    except Exception:
                        pass

                # Merge: per-experiment results.json takes precedence
                merged_metrics = {**exp_metrics, **per_exp_results}

                # Collect figures
                figures = self._collect_figures(exp_dir)

                # Narrative from analysis
                narrative = ""
                if analysis:
                    narrative = str(analysis.get("narrative", ""))[:600]

                tex_path = self._write_experiment_tex(
                    index=i,
                    exp_name=exp_name,
                    exp_results=merged_metrics,
                    figures=figures,
                    narrative=narrative,
                    domain=manifest.get("domain", "general_ml"),
                    paper_dir=paper_dir,
                )
                # \input path: relative to paper_dir if given, else absolute
                if paper_dir:
                    try:
                        rel = os.path.relpath(tex_path, paper_dir)
                        input_path = rel.replace("\\", "/")
                    except ValueError:
                        input_path = str(tex_path).replace("\\", "/")
                else:
                    input_path = str(tex_path).replace("\\", "/")

                input_lines.append(f"\\input{{{input_path}}}")
            except Exception as exc:
                log.warning("tex_output: skipping experiment %s: %s", exp.get("name"), exc)
                continue

        # Write combined file
        combined_path = self.tex_output_dir / "experiments_combined.tex"
        combined_content = (
            "% Auto-generated by ARAS ExperimentTexGenerator\n"
            "% Include this file in your Experiments section\n\n"
            + "\n".join(input_lines) + "\n"
        )
        combined_path.write_text(combined_content, encoding="utf-8")
        log.info("tex_output: wrote %d experiment sections → %s", len(input_lines), combined_path)
        return combined_path

    # ------------------------------------------------------------------
    # Private methods
    # ------------------------------------------------------------------

    def _collect_figures(self, exp_dir: Path) -> list[Path]:
        """Collect .png and .pdf figures from an experiment directory."""
        figures: list[Path] = []
        if not exp_dir.exists():
            return figures
        # Top-level figures
        figures.extend(sorted(exp_dir.glob("*.png")))
        figures.extend(sorted(exp_dir.glob("*.pdf")))
        # Subfolder figures/
        sub = exp_dir / "figures"
        if sub.exists():
            figures.extend(sorted(sub.glob("*.png")))
            figures.extend(sorted(sub.glob("*.pdf")))
        return figures

    def _write_experiment_tex(
        self,
        index: int,
        exp_name: str,
        exp_results: dict[str, Any],
        figures: list[Path],
        narrative: str,
        domain: str,
        paper_dir: Path | None = None,
    ) -> Path:
        """Write a single experiment's .tex section file."""
        human_name = exp_name.replace("_", " ").title()
        # Remove leading "Exp1 " prefix if present
        human_name = re.sub(r"^Exp\d+\s+", "", human_name)

        lines: list[str] = [
            f"% Auto-generated by ARAS — {exp_name}",
            f"\\subsection{{Experiment {index}: {human_name}}}",
            "",
        ]

        # Results table
        metrics = exp_results.get("metrics", exp_results)
        comparison = exp_results.get("comparison_table", [])

        if isinstance(comparison, list) and comparison:
            lines += self._build_comparison_table(comparison)
        elif isinstance(metrics, dict) and metrics:
            # Filter out non-scalar metrics for table display
            scalar_metrics = {
                k: v for k, v in metrics.items()
                if isinstance(v, (int, float, str))
                and k not in ("name", "exit_code", "stdout", "stderr", "artifacts", "experiment")
            }
            if scalar_metrics:
                lines += self._build_metrics_table(exp_name, scalar_metrics)

        lines.append("")

        # Figures — paths relative to paper_dir or tex_output_dir
        ref_dir = paper_dir if paper_dir else self.tex_output_dir
        for j, fig_path in enumerate(figures[:3], start=1):
            try:
                rel = os.path.relpath(fig_path, ref_dir)
                rel_posix = rel.replace("\\", "/")
            except ValueError:
                # Different drive on Windows
                rel_posix = str(fig_path).replace("\\", "/")

            cap = exp_name.replace("_", " ").title()
            lines += [
                "\\begin{figure}[htbp]",
                "  \\centering",
                f"  \\includegraphics[width=0.9\\linewidth]{{{rel_posix}}}",
                f"  \\caption{{Results for {cap}.}}",
                f"  \\label{{fig:{exp_name}_fig{j}}}",
                "\\end{figure}",
                "",
            ]

        # Narrative paragraph
        if narrative.strip():
            safe_narrative = self._escape_latex(narrative)
            lines += ["", safe_narrative, ""]

        # Runtime note
        runtime = exp_results.get("wall_seconds") or exp_results.get("runtime_seconds")
        timeout = exp_results.get("TIMEOUT_SAFE", False)
        if runtime is not None:
            try:
                runtime_f = float(runtime)
                note = f"Runtime: {runtime_f:.1f}s"
                if timeout:
                    note += " (partial results — timeout reached)"
                lines.append(f"\\textit{{{note}}}")
                lines.append("")
            except (TypeError, ValueError):
                pass

        tex_content = "\n".join(lines) + "\n"
        out_path = self.tex_output_dir / f"{exp_name}.tex"
        out_path.write_text(tex_content, encoding="utf-8")
        return out_path

    def _build_comparison_table(self, rows: list[dict[str, Any]]) -> list[str]:
        """Build a \\begin{table} from comparison_table list of dicts."""
        if not rows:
            return []
        headers = list(rows[0].keys())
        col_spec = "l" + "c" * (len(headers) - 1)
        header_row = " & ".join(
            f"\\textbf{{{self._escape_latex(h.replace('_', ' ').title())}}}"
            for h in headers
        )
        lines = [
            "\\begin{table}[htbp]",
            "  \\centering",
            f"  \\begin{{tabular}}{{{col_spec}}}",
            "    \\toprule",
            f"    {header_row} \\\\",
            "    \\midrule",
        ]
        for row in rows:
            vals: list[str] = []
            for h in headers:
                v = row.get(h, "")
                if isinstance(v, float):
                    vals.append(f"{v:.4f}")
                else:
                    vals.append(self._escape_latex(str(v)))
            lines.append("    " + " & ".join(vals) + " \\\\")
        lines += [
            "    \\bottomrule",
            "  \\end{tabular}",
            "  \\caption{Comparison of methods.}",
            "  \\label{tab:comparison}",
            "\\end{table}",
        ]
        return lines

    def _build_metrics_table(self, exp_name: str, metrics: dict[str, Any]) -> list[str]:
        """Build a simple two-column metrics table."""
        lines = [
            "\\begin{table}[htbp]",
            "  \\centering",
            "  \\begin{tabular}{lc}",
            "    \\toprule",
            "    \\textbf{Metric} & \\textbf{Value} \\\\",
            "    \\midrule",
        ]
        for k, v in list(metrics.items())[:10]:
            if isinstance(v, float):
                val = f"{v:.4f}"
            elif isinstance(v, (int, str)):
                val = str(v)
            else:
                continue
            safe_k = self._escape_latex(k.replace("_", " ").title())
            lines.append(f"    {safe_k} & {val} \\\\")
        lines += [
            "    \\bottomrule",
            "  \\end{tabular}",
            f"  \\caption{{Metrics for {self._escape_latex(exp_name.replace('_', ' ').title())}.}}",
            f"  \\label{{tab:{exp_name}_metrics}}",
            "\\end{table}",
        ]
        return lines

    @staticmethod
    def _escape_latex(text: str) -> str:
        """Escape common LaTeX special characters in free text."""
        replacements = [
            ("\\", "\\textbackslash{}"),
            ("&", "\\&"), ("%", "\\%"), ("$", "\\$"),
            ("#", "\\#"), ("_", "\\_"), ("{", "\\{"),
            ("}", "\\}"), ("~", "\\textasciitilde{}"),
            ("^", "\\textasciicircum{}"),
        ]
        for char, escaped in replacements:
            text = text.replace(char, escaped)
        return text
